
public class Student {
    int fys;
    int language;
    int humanity;
    int society;
    int science;
    int personal;
    int commonTheme;
    int extra;

    int majorA;
    int majorB;
    int majorC;
    int related;
    int freeChoice;

    public Student() {
    }

    public Student(int fys, int language, int humanity, int society, int science, int personal, int commonTheme,
            int extra, int majorA, int majorB, int majorC, int related, int freeChoice) {
        this.fys = fys;
        this.language = language;
        this.humanity = humanity;
        this.society = society;
        this.science = science;
        this.personal = personal;
        this.commonTheme = commonTheme;
        this.extra = extra;
        this.majorA = majorA;
        this.majorB = majorB;
        this.majorC = majorC;
        this.related = related;
        this.freeChoice = freeChoice;
    }

}
